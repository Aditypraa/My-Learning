// * Constructor Function
// Ini adalah pembuatan CLASS di JAVASCRIPT lama
function Person() {
  // PROPERTY
  this.firstName = "";
  this.lastName = "";
}

// Object
let orang1 = new Person();
// Mengubah property dari class Person
orang1.firstName = "Budiman";
orang1.lastName = "Pratama";
console.log(orang1);

// Object
let orang2 = new Person();
// Mengubah property dari class Person
orang2.firstName = "Asep";
orang2.lastName = "Saputra";
console.log(orang2);
