let angka1 = 20;
let angka2 = 30;

// Lebih Dari atau Lebih Besar Dari
console.log(angka1 > angka2);

// Kurang Dari atau Lebih Kecil dari
console.log(angka1 < angka2);

// Lebih dari sama dengan atau lebih besar sama dengan
console.log(angka1 >= angka2);

// Kurang dari sama dengan atau lebih kecil sama dengan
console.log(angka1 <= angka2);

// Sama dengan
console.log(angka1 == angka2);

// Sama dengan atau bernilai sama dan sejenis
console.log(angka1 === angka2);

// Tidak sama dengan dari
console.log(angka1 != angka2);

// Tidak sama dengan dan sejenis
console.log(angka1 !== angka2);
