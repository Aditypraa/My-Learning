let number1 = 150;
let number2 = Number("150");
let jumlahNumber = number1 + number2;
console.log(
  `Hasil dari number1 + number2 adalah ${jumlahNumber} dengan Tipe Data ${typeof jumlahNumber}`
);

let string1 = "10";
let string2 = String(10);
let hasilString = string1 + string2;
console.log(`Hasil Dari penambahan string1 + string2 adalah ${hasilString}`);

let boolean = 15;
boolean = Boolean(boolean);
console.log(
  `Variabel boolean berisi ${boolean} dengan Tipe Data ${typeof boolean}`
);
