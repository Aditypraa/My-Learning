function belajarSwitch() {
  let nilai = 20;

  switch (nilai) {
    case 10:
      console.log("Nilai Mata Kuliah kurang baik");
      break;
    case 20:
      console.log("Nilai Mata kuliah anda baik");
      break;
    default:
      console.log("Nilai mata kuliah anda Melampaui batas");
  }
}

belajarSwitch();
