function comparison() {
  let a = 10;
  let b = 5;

  console.log(a > b); // Apakah a lebih besar dari b
  console.log(a < b); // Apakah a Lebih Kecil dari b
  console.log(a >= b); // apakah a lebih besar sama dengan b
  console.log(a <= b); // Apakah a Lebih Kecil sama dengan b
  console.log(a == b); // apakah a sama dengan b
  console.log(a === b); // apakah a sama dengan b dengan mengecek tipe data juga
  console.log(a != b); // apakah a tidak sama dengan b
  console.log(a !== b); // apakah a tidak sama dengan b dengan mengecek tipe data juga
}

comparison();
