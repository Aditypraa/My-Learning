// ARITMATIKA
let result = 1 + 2; // Penambahan
console.log(`Hasil ${result}`);

result = result - 1; // Pengurangan
console.log(`Hasil : ${result}`);

result = result * 2; // Perkalian
console.log(`Hasil ${result}`);

result = result / 2; // Pembagian
console.log(`Hasil ${result}`);

// Operator Augmented Assignment
let coba = 1 + 2;
console.log(coba);

coba -= 1;
console.log(coba);

coba *= 2;
console.log(coba);

coba /= 2;
console.log(coba);

// OPerator Unary
let unary = +1;
console.log(unary);

unary++;
console.log(unary);

unary--;
console.log(unary);

unary = -unary;
console.log(unary);
